/**
 * largest_number - returns the largest of 3 numbers
 * @a: first integer
 * @b: second integer
 * @c: third integer
 * Return: largest integer
 */
int largest_number(int a, int b, int c)
{
	int largest;
	int x;

	if (a > b)
	{
		x = a;
	}
	else
	{
		x = b;
	}
	if (x > c)
	{
		largest = x;
	}
	else
	{
		largest = c;
	}
	return (largest);
}
